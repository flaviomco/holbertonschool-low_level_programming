#include "operations.h"

/**
 * add - Adds two integers.
 * @a: The first integer to be added.
 * @b: The second integer to be added.
 *
 * Return: The result of the addition.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Adds two integers.
 * @a: The first integer to be added.
 * @b: The second integer to be added.
 *
 * Return: The result of the addition.
 */
int sub(int a, int b)
{
        return (a - b);
}

/**
 * mul- Adds two integers.
 * @a: The first integer to be added.
 * @b: The second integer to be added.
 *
 * Return: The result of the addition.
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Adds two integers.
 * @a: The first integer to be added.
 * @b: The second integer to be added.
 *
 * Return: The result of the addition.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Adds two integers.
 * @a: The first integer to be added.
 * @b: The second integer to be added.
 *
 * Return: The result of the addition.
 */
int mod(int a, int b)
{
	return (a % b);
}
